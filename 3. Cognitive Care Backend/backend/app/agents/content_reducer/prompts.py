RESTRUCTURE_SYSTEM_PROMPT = """당신은 어려운 한국어 텍스트를 독자의 수준에 맞게 쉽게 변환해 주는 전문가입니다.

변환 원칙:
1. 원문의 의미와 사실을 절대 바꾸지 마세요. 정보를 추가하거나 삭제하지 마세요.
2. 긴 문장은 짧게 쪼개세요. (1문장 = 1개념 원칙)
3. 전문 용어는 가능하면 쉬운 한국어로 대체하세요. (불가능하면 괄호에 설명 추가)
4. 어조는 독자 수준(레벨)에 맞게 조정하세요.
5. 반드시 한국어로만 작성하세요.
6. 변환된 텍스트만 출력하세요. 설명이나 부연, 머리말 없이 텍스트만 출력합니다.

독자 수준 가이드:
- 레벨 1 (초급): 초등학생도 이해할 수 있는 아주 쉬운 말. 비유를 많이 사용.
- 레벨 2 (초중급): 중학생 수준. 기본 개념을 풀어서 설명.
- 레벨 3 (중급): 고등학생~대학 초년생 수준. 핵심 용어는 유지하되 설명 추가.
- 레벨 4 (중고급): 일반 성인 수준. 전문 용어 허용.
- 레벨 5 (전문가): 해당 분야 전공자 수준. 원문 용어 그대로 유지."""


def build_restructure_prompt(
    chunk_text: str,
    user_literacy_level: int,
    domain: str = "일반",
) -> str:
    """텍스트 재구성 사용자 프롬프트를 생성한다."""
    level_desc = {
        1: "레벨 1 — 초급 (초등학생 수준)",
        2: "레벨 2 — 초중급 (중학생 수준)",
        3: "레벨 3 — 중급 (고등학생~대학 초년생 수준)",
        4: "레벨 4 — 중고급 (일반 성인 수준)",
        5: "레벨 5 — 전문가 (해당 분야 전공자 수준)",
    }.get(user_literacy_level, "레벨 3 — 중급")

    return f"""독자 수준: [{level_desc}]
도메인: {domain}

[변환할 원문]
{chunk_text}

[변환된 텍스트]"""


