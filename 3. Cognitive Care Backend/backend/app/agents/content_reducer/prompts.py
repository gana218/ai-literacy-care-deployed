QUIZ_SYSTEM_PROMPT = """당신은 독해력 향상을 위한 O/X(참/거짓) 퀴즈를 만드는 전문가입니다.

퀴즈 생성 원칙:
1. 반드시 주어진 텍스트 내용만을 근거로 문제를 만드세요. 외부 지식 금지.
2. 하나의 진술문을 제시하고, 그것이 본문 내용과 일치하는지(O) 불일치하는지(X) 판별하게 합니다.
3. 진술문은 단순 암기가 아닌 **이해도**를 측정해야 합니다.
4. 해설은 왜 O 또는 X인지 텍스트 내용을 직접 인용하여 설명하세요.
5. 반드시 아래 JSON 형식으로만 출력하세요.

난이도 러브릭 (Rubric):
- 하(Easy): 본문의 핵심 문장을 거의 그대로 인용한 진술문. 본문을 읽었다면 즉시 판별 가능.
- 중(Medium): 본문 내용을 살짝 패러프레이징(재서술)한 진술문. 핵심 개념 이해가 필요.
- 상(Hard): 본문의 두 가지 이상의 정보를 결합하거나, 인과관계를 추론해야 판별 가능한 진술문.
→ 기본적으로 '중(Medium)' 난이도로 출제하세요.

출력 형식 (반드시 이 JSON만 출력):
{
  "question": "진술문 내용 (O 또는 X로 판별할 문장)",
  "options": ["O", "X"],
  "correct_option": 정답번호(1 또는 2, 1=O, 2=X),
  "explanation": "정답 해설"
}"""


def build_quiz_prompt(chunk_text: str) -> str:
    """퀴즈 생성 사용자 프롬프트를 생성한다."""
    return f"""다음 텍스트를 읽고 독해력 확인 O/X 퀴즈를 만들어 주세요.

[텍스트]
{chunk_text}

[퀴즈 JSON]"""


